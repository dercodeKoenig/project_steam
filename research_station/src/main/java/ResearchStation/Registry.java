package ResearchStation;

import net.minecraft.core.registries.BuiltInRegistries;
import net.minecraft.world.item.BlockItem;
import net.minecraft.world.item.Item;
import net.minecraft.world.level.block.Block;
import net.minecraft.world.level.block.entity.BlockEntityType;
import net.neoforged.bus.api.IEventBus;

import java.util.function.Supplier;

public class Registry {
    public static final net.neoforged.neoforge.registries.DeferredRegister<Block> BLOCKS = net.neoforged.neoforge.registries.DeferredRegister.create(BuiltInRegistries.BLOCK, "research_station");
    public static final net.neoforged.neoforge.registries.DeferredRegister<BlockEntityType<?>> BLOCK_ENTITIES = net.neoforged.neoforge.registries.DeferredRegister.create(BuiltInRegistries.BLOCK_ENTITY_TYPE, "research_station");
    public static final net.neoforged.neoforge.registries.DeferredRegister<Item> ITEMS = net.neoforged.neoforge.registries.DeferredRegister.create(BuiltInRegistries.ITEM, "research_station");

    public static Supplier<Item> registerBlockItem(String name, Supplier<Block> b){
        return ITEMS.register(name,() -> new BlockItem(b.get(), new Item.Properties()));
    }

    public static final Supplier<Block> RESEARCH_STATION = BLOCKS.register(
            "research_station",
            () -> new BlockResearchStation()
    );
    public static final Supplier<BlockEntityType<EntityResearchStation>> ENTITY_RESEARCH_STATION = BLOCK_ENTITIES.register(
            "entity_research_station",
            () -> BlockEntityType.Builder.of(EntityResearchStation::new, RESEARCH_STATION.get()).build(null)
    );

    public static final Supplier<Item> ITEM_RESEARCH_BOOK = ITEMS.register(
            "research_book",
            () -> new ItemResearchBook()
    );


    static {
        registerBlockItem("research_station", RESEARCH_STATION);
    }

    public static void register(IEventBus modBus) {
        BLOCKS.register(modBus);
        ITEMS.register(modBus);
        BLOCK_ENTITIES.register(modBus);
    }

}
